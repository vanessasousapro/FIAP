jedi = ["Yoda", "Luke", "Obi-Wan", "Anakin"]

#Remoção do último valor
jedi.pop()

# Remoção de um valor em posição específica
jedi.pop(1) #passe como argumento o índice do item que quer remover

#Remoção de um item específico
jedi.remove("Yoda") #passe como argumento o conteúdo do item que quer remover

#Apagar a lista toda
jedi.clear() #retorna uma lista vazia.
